package org.eclipsetrader.ui.internal.repositories;

public class WatchListContainerObject {
	private String name;

	public WatchListContainerObject(String name) {
		this.name = name;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		return name.hashCode();
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (obj instanceof WatchListContainerObject)
			return name.equals(((WatchListContainerObject) obj).name);
		if (obj instanceof String)
			return name.equals(obj);
		return false;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return name;
	}
}
